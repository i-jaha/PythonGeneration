text = input()
words = text.split(' ')
result = []

for word in words:
    shift = sum(c.isalpha() for c in word)
    encrypted = ''
    for char in word:
        if char.isalpha():
            base = ord('A') if char.isupper() else ord('a')
            encrypted += chr((ord(char) - base + shift) % 26 + base)
        else:
            encrypted += char
    result.append(encrypted)

print(' '.join(result))
from math import sqrt
def solve(a, b, c):
    D = b ** 2 - 4 * a * c
    if D == 0:
        x = -b / (2 * a)
        return x, x
    else:
        x1 = (-b - sqrt(D)) / (2 * a)
        x2 = (-b + sqrt(D)) / (2 * a)
        if x1 > x2:
            x1, x2 = x2, x1
        return x1, x2

